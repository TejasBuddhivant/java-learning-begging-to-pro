class Operators{
public static void main(String... arr){
int a =3, b=5;
System.out.println(a<b);
System.out.println(a>b);
System.out.println(a<=b);
System.out.println(a>=b);
System.out.println(a!=b);
System.out.println((a==b));
System.out.println();
System.out.println((a>b)&&(a<b));
System.out.println((a>=b)&&(a<=b));
System.out.println((a>b)&&(a!=b));
System.out.println((a!=b)&&(a==b));
System.out.println();
System.out.println((a>b)||(a<b));
System.out.println((a>=b)||(a<=b));
System.out.println((a>b)||(a!=b));
System.out.println((a!=b)||(a==b));

}
}